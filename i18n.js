let currentLang = 'en';

function applyLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  // Update canonical URL
  const canonical = document.querySelector('link[rel="canonical"]');
  if (canonical) {
    canonical.href = lang === 'en' 
      ? 'https://agency.munister.com.ua/' 
      : `https://agency.munister.com.ua/?lang=${lang}`;
  }

  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else if (el.tagName === 'OPTION') {
        el.textContent = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  localStorage.setItem('lang', lang);

  // Update URL params without reloading
  const url = new URL(window.location);
  if (lang === 'en') {
    url.searchParams.delete('lang');
  } else {
    url.searchParams.set('lang', lang);
  }
  window.history.replaceState({}, '', url);
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

const urlLang = new URLSearchParams(window.location.search).get('lang');
const requested = ['uk', 'en', 'de', 'pl', 'es', 'pt', 'it'].includes(urlLang) ? urlLang : null;
const saved = localStorage.getItem('lang');
const browser = navigator.language.slice(0, 2);
const preferred = requested || saved || (['uk','en','de','pl','es','pt','it'].includes(browser) ? browser : 'en');
applyLang(preferred);
